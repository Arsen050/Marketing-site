const question__list = document.querySelector(".question__list");

question__list.addEventListener("click", function (e) {
	if (
		e.target.className === "question__item-chevron-down" ||
		e.target.className ===
			"question__item-chevron-down question__item-chevron-top"
	) {
		const item = e.target.closest(".question__item").childNodes;

		item.forEach((element) => {
			if (element.className === "question__answer-text") {
				if (element.style.display === "" || element.style.display === "none") {
					element.style.display = "block";
					e.target.classList.add("question__item-chevron-top");
				} else if (element.style.display === "block") {
					element.style.display = "none";
					e.target.classList.remove("question__item-chevron-top");
				}
			}
		});
	}
});
